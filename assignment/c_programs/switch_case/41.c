//41.Write a C program to check whether an alphabet is vowel or consonant using switch case



#include<stdio.h>
void main()
{
	char ch;
	printf("enter a character:\n");
	scanf("%c",&ch);
	switch(ch)
	{
		case'a':

		case'e':

		case'i':

		case'o':

		case'u':


			printf("is a vowel\n");
			break;

		default:
			printf("is a consonent\n");
	}
}
